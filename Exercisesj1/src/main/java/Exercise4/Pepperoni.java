package Exercise4;

public class Pepperoni {
	
	private String Name;
	private String Description;
	private double Price;
	private String Ingredients;
	private String discount;
	
	public String getPepperoniName() {
		return Name;
	}
	public void setPepperoniName(String name) {
		Name = name;
	}
	public String getPepperoniDescription() {
		return Description;
	}
	public void setPepperoniDescription(String description) {
		Description = description;
	}
	public double getPepperoniPrice() {
		return Price;
	}
	public void setPepperoniPrice(double price) {
		Price = price;
	}
	public String getPepperoniIngredients() {
		return Ingredients;
	}
	public void setPepperoniIngredients(String ingredients) {
		Ingredients = ingredients;
	}
	public String getPepperoniDiscount() {
		return discount;
	}
	public void setPepperoniDiscount(String discount) {
		this.discount = discount;
	}
	

}
