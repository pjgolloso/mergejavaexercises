package Exercise4;

public class Buffalo {
	
	private String name;
	private String Description;
	private double Price;
	private String Ingredients;
	private String discount;
	
	
	public String getBuffaloName() {
		return name;
	}
	public void setBuffaloName(String name) {
		this.name = name;
	}
	public String getBuffaloDescription() {
		return Description;
	}
	public void setBuffaloDescription(String description) {
		Description = description;
	}
	public double getBuffaloPrice() {
		return Price;
	}
	public void setBuffaloPrice(double price) {
		Price = price;
	}
	public String getBuffaloIngredients() {
		return Ingredients;
	}
	public void setBuffaloIngredients(String ingredients) {
		Ingredients = ingredients;
	}
	public String getBuffaloDiscount() {
		return discount;
	}
	public void setBuffaloDiscount(String discount) {
		this.discount = discount;
	}
	
	
	
	
}
